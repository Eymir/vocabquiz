    private void loadResults() {
        String results = "";
        
        BufferedReader inFile = null;
        
        try {
            inFile = new BufferedReader(new FileReader("VocabQuizResults.res"));
            
            while(true) {
                String l = inFile.readLine();
                if (l == null) break;
                results += (l + "\n");
            }
            
            inFile.close();
        }
        
        catch (IOException ex) {
            // file probably doesn't exist; ignore quietly
            if (inFile != null){
                try {
                    inFile.close();
                } catch (IOException e) {
                    // ignore
                }
            }
        }
        
        resultsList.setText(results);
    }
    
