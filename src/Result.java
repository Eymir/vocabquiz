    private void addScoreLine(String line) {
        BufferedWriter outFile = null;
        
        try {
            outFile = new BufferedWriter(
                    new FileWriter("VocabQuizResults.res", true));
            
            outFile.write(line + "\n");
            
            outFile.flush();
            outFile.close();
            
            saveButton.setEnabled(false);
        }
        
        catch (IOException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                    "Could not write to results file: VocabQuizResults.res",
                    "Failed to save results",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            
            if (outFile != null){
                try {
                    outFile.flush();
                    outFile.close();
                } catch (IOException e) {
                    // ignore
                }
            }
        }
    }
    
